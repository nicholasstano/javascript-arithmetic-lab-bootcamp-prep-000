function add(a, b) {
  return a + b 
}
//returns the sum of the inputted values a and b.

function subtract(a, b) {
  return a - b
}
//returns the subtraction of b from a 

function multiply(a, b) {
  return a*b
}
//returns the multiplication of a and b 

function divide(a, b) {
  return a/b 
}
//returns the divison of b by a

function inc(n) {
  return n +=1 
}
//adds 1 to n and returns the result

function dec(n) {
  return n -= 1
}
//subtracts 1 from n and returns the result.

function makeInt(n) {
  return parseInt(n, 10)
}
//returns n as an integer where n =/= string

